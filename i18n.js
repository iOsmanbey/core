// Get current language from localStorage or default to English
let currentLang = localStorage.getItem('medico_lang') || 'en';

// Function to translate all elements with data-i18n attribute
function translatePage() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.textContent = translations[currentLang][key];
        }
    });

    // Translate placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.placeholder = translations[currentLang][key];
        }
    });

    // Update language button
    const langButton = document.getElementById('current-lang');
    if (langButton) {
        langButton.textContent = currentLang.toUpperCase();
    }
}

// Language toggle function
function toggleLanguage() {
    const langs = ['en', 'ru', 'uz'];
    const currentIndex = langs.indexOf(currentLang);
    const nextIndex = (currentIndex + 1) % langs.length;
    currentLang = langs[nextIndex];

    // Save to BOTH keys for compatibility
    localStorage.setItem('medico_lang', currentLang);
    localStorage.setItem('language', currentLang);

    // Translate all static elements
    translatePage();

    // Re-render dynamic content (products, news, etc.) with new language
    // These functions are defined in script.js
    if (typeof renderProducts === 'function') {
        renderProducts();
    }
    if (typeof renderNewArrivals === 'function') {
        renderNewArrivals();
    }
    if (typeof renderNews === 'function') {
        renderNews();
    }
}

// Note: renderProducts is defined in script.js - do not duplicate here

document.addEventListener('DOMContentLoaded', () => {
    // Sync language keys on load
    const savedLang = localStorage.getItem('medico_lang') || localStorage.getItem('language') || 'en';
    currentLang = savedLang;
    localStorage.setItem('medico_lang', currentLang);
    localStorage.setItem('language', currentLang);

    // Initial translation
    translatePage();

    // Attach event listener to language toggle button
    const langToggleBtn = document.getElementById('lang-toggle');
    if (langToggleBtn) {
        langToggleBtn.addEventListener('click', (e) => {
            e.preventDefault();
            toggleLanguage();
        });
    }
});
